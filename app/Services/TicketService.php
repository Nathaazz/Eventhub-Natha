<?php

namespace App\Services;

use App\Models\Ticket;
use App\Models\Registration;

use Illuminate\Support\Str;

class TicketService
{
    protected $qrService;

    public function __construct(
        QRCodeService $qrService
    )
    {
        $this->qrService = $qrService;
    }

    /*
    |--------------------------------------------------------------------------
    | GENERATE TICKET
    |--------------------------------------------------------------------------
    */

    public function generateForRegistration(
        Registration $registration
    ): Ticket
    {

        /*
        |--------------------------------------------------------------------------
        | CEGAH DUPLICATE TICKET
        |--------------------------------------------------------------------------
        */

        $existing = Ticket::where(

                'registration_id',

                $registration->id

            )
            ->first();

        if ($existing) {

            return $existing;

        }

        /*
        |--------------------------------------------------------------------------
        | GENERATE UNIQUE CODE
        |--------------------------------------------------------------------------
        */

        $ticketCode =

            'EVT-' .

            Str::upper(
                Str::random(10)
            );

        /*
        |--------------------------------------------------------------------------
        | CREATE TICKET
        |--------------------------------------------------------------------------
        */

        $ticket = Ticket::create([

            /*
            |--------------------------------------------------------------------------
            | RELATION
            |--------------------------------------------------------------------------
            */

            'registration_id' =>
                $registration->id,

            'event_id' =>
                $registration->event_id,

            /*
            |--------------------------------------------------------------------------
            | TICKET
            |--------------------------------------------------------------------------
            */

            'ticket_code' =>
                $ticketCode,

            /*
            |--------------------------------------------------------------------------
            | STATUS
            |--------------------------------------------------------------------------
            */

            'status' => 'active',

        ]);

        /*
        |--------------------------------------------------------------------------
        | GENERATE QR
        |--------------------------------------------------------------------------
        */

        $qrPath = $this->qrService
            ->generateTicketQR($ticket);

        /*
        |--------------------------------------------------------------------------
        | SAVE QR PATH
        |--------------------------------------------------------------------------
        */

        $ticket->update([

            'qr_code_path' => $qrPath

        ]);

        return $ticket;
    }
}
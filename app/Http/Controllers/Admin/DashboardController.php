<?php

namespace App\Http\Controllers\Admin;

use App\Http\Controllers\Controller;

use App\Models\Event;
use App\Models\Registration;
use App\Models\User;
use App\Models\Ticket;
use App\Models\Certificate;

class DashboardController extends Controller
{
    public function index()
    {
        /*
        |--------------------------------------------------------------------------
        | STATS
        |--------------------------------------------------------------------------
        */

        $stats = [

            /*
            |--------------------------------------------------------------------------
            | TOTAL USER
            |--------------------------------------------------------------------------
            */

            'total_users' => User::count(),


            /*
            |--------------------------------------------------------------------------
            | TOTAL EVENT
            |--------------------------------------------------------------------------
            */

            'total_events' => Event::count(),


            /*
            |--------------------------------------------------------------------------
            | TOTAL PESERTA
            |--------------------------------------------------------------------------
            */

            'total_registrations' => Registration::where('status', 'approved')->count(),


            /*
            |--------------------------------------------------------------------------
            | TOTAL TICKET
            |--------------------------------------------------------------------------
            */

            'total_tickets' => class_exists(Ticket::class)
                ? Ticket::count()
                : 0,


            /*
            |--------------------------------------------------------------------------
            | TOTAL CERTIFICATE
            |--------------------------------------------------------------------------
            */

            'total_certificates' => class_exists(Certificate::class)
                ? Certificate::count()
                : 0,


            /*
            |--------------------------------------------------------------------------
            | RECENT EVENTS
            |--------------------------------------------------------------------------
            */

            'recent_events' => Event::with('organizer')
                ->latest()
                ->take(5)
                ->get(),


            /*
            |--------------------------------------------------------------------------
            | RECENT REGISTRATIONS
            |--------------------------------------------------------------------------
            */

            'recent_registrations' => Registration::with([
                    'user',
                    'event'
                ])
                ->latest()
                ->take(5)
                ->get(),
        ];


        /*
        |--------------------------------------------------------------------------
        | RETURN VIEW
        |--------------------------------------------------------------------------
        */

        return view('admin.dashboard', compact('stats'));
    }
}